public class AuthorQuotesItem
{
    private String category_id;
    private String quote_likes_count;
    private String quote_created_on;
    private String quote_id;
    private String category_name;
    private String quote;



    public String getCategory_id() {
        return category_id;
    }

    public void setCategory_id(String category_id) {
        this.category_id = category_id;
    }

    public String getQuote_likes_count() {
        return quote_likes_count;
    }

    public void setQuote_likes_count(String quote_likes_count) {
        this.quote_likes_count = quote_likes_count;
    }

    public String getQuote_created_on() {
        return quote_created_on;
    }

    public void setQuote_created_on(String quote_created_on) {
        this.quote_created_on = quote_created_on;
    }

    public String getQuote_id() {
        return quote_id;
    }

    public void setQuote_id(String quote_id) {
        this.quote_id = quote_id;
    }

    public String getCategory_name() {
        return category_name;
    }

    public void setCategory_name(String category_name) {
        this.category_name = category_name;
    }

    public String getQuote() {
        return quote;
    }

    public void setQuote(String quote) {
        this.quote = quote;
    }


}




